@dataclass
class PromptInput:
    age_tier: str  # "8-11" | "12-14"
    mode: str      # chat | story | encyclopedia | emotion
    strategy: str  # 策略 ID
    memory_context: str | None  # Step 0 输出的记忆上下文（无则 None）


def build_system_prompt(prompt_input: PromptInput) -> str:
    """构建完整 system prompt

    顺序：
        1. 基础角色 + 年龄适配
        2. 模式行为规则
        3. 固定反谄媚指令
        4. 策略指令
        5. 记忆上下文（仅在非空时注入）
    """
    parts: list[str] = []

    # 1. 基础角色 + 年龄适配
    age_key = prompt_input.age_tier if prompt_input.age_tier in AGE_BASE_PROMPTS else "8-11"
    parts.append(AGE_BASE_PROMPTS[age_key])

    # 2. 模式行为规则
    mode_key = prompt_input.mode if prompt_input.mode in MODE_RULES else "chat"
    parts.append(MODE_RULES[mode_key])

    # 3. 固定反谄媚指令
    parts.append(ANTI_SYCOPHANCY_RULES)

    # 4. 策略指令
    strategy_key = prompt_input.strategy if prompt_input.strategy in STRATEGY_INSTRUCTIONS else pe.STRATEGY_NORMAL
    parts.append(STRATEGY_INSTRUCTIONS[strategy_key])

    # 5. 记忆上下文
    if prompt_input.memory_context:
        parts.append(
            "## 星球记忆（背景知识，仅在话题相关时引用）\n"
            f"{prompt_input.memory_context}\n"
            "——仅当话题自然相关时才提及，不要为引用而引用。"
        )

    return "\n\n".join(parts)
